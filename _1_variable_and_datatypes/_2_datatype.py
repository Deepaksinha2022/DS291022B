# Datatypes

# int - digit            
# float - decimal
# str - word,character,statment,para
# bool - boolean (True(1)/False(0))
# complex - real + imaginary
# NoneType - void

# list
# tuple
# set
# dict

# int
# immutable
num = 2
print(type(num))

# float
# immutable
salary = 15000.50
salary_type = type(salary)
print(salary_type)

# str
# immutable
name = "Bharati"
name_type = type(name)
print(name_type)

# bool
# immutable
female = True
female_type = type(female)
print(female_type)

# complex
# immutable
data = 2+1j
data_type = type(data)
print(data_type)

hobby = None
hobby_type = type(hobby)
print(hobby_type)
