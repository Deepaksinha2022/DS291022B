# Arithemetic Operator
# +
# - 
# *
# / 
# %  - Modulus (Mod)
# // - Floor Division
# ** - Exponential

no1 = 30
no2 = 3

add = no1 + no2
print("Addition : ",add)

sub = no1 - no2
print("Subtraction : ",sub)

mul = no1 * no2
print("Multiplication : ",mul)

div = no1 / no2            # / operator returns the float value
print("Division : ",div)

mod = no1 % no2            # returns remainder
print("Modulus : ",mod)

floor_div = no1 // no2     # returns int after division, ignores values after .
print("Floor Division : ",floor_div)

exponential = no1 ** no2   # raise to power
print("Exponential : ",exponential)


