# Membership Operator

# in      # returns True when an object is present inside the sequence of object else False
# not in  # returns True when an object is not present inside the sequence of object else False


no1 = 10
no2 = [6,7,1,3,10,5]

in_demo = no1 in no2
print("In : ",in_demo)

not_in_demo = no1 not in no2
print("Not In : ",not_in_demo)