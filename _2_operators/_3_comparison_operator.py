# Comparison Operator - returns bool value

# ==
# >
# <
# >=
# <=
# !=

no1 = 90
no2 = 78

equal = no1 == no2
print("Equal : ",equal)

not_equal = no1 != no2
print("Not Equal : ",not_equal)

greater_than = no1 > no2
print("Greater than : ",greater_than)

less_than = no1 < no2
print("Less than : ",less_than)

greater_than_equal = no1 > no2
print("Greater than equal to : ",greater_than_equal)

less_than_equal = no1 < no2
print("Less than equal to : ",less_than_equal)